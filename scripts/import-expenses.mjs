async function importExpenses() {
  const col = collection(db, 'expenses');
  let count = 0;
  let failed = 0;

  for (const exp of expenses) {
    try {
      await addDoc(col, {
        date: new Date(exp.date).toISOString(),
        description: exp.description,
        amount: exp.amount,
        category: exp.category,
      });
      count++;
      if (count % 10 === 0) console.log(`✓ ${count}/${expenses.length} imported...`);
    } catch (err) {
      console.error(`✗ Failed: ${exp.description}`, err.message);
      failed++;
    }
  }

  console.log(`\nDone! ${count} expenses imported, ${failed} failed.`);
  console.log(`Total amount: PKR ${expenses.reduce((s, e) => s + e.amount, 0).toLocaleString()}`);
  process.exit(0);
}

importExpenses();
