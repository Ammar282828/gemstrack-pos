export function normalizeSpaces(value) {
  return String(value ?? '').replace(/\s+/g, ' ').trim();
}

export function isAlreadyStructured(description) {
  return STRUCTURED_PREFIXES.some(prefix => description.startsWith(prefix));
}

export function fallbackRename(description, category) {
  switch (category) {
    case 'Rent':
      return `Rent - ${description}`;
    case 'Marketing':
      return `Marketing - ${description}`;
    case 'Salaries':
      return `Salary - ${description}`;
    case 'Travel':
      return `Transport - ${description}`;
    case 'Other':
      return `Payment - ${description}`;
    case 'Supplies':
      return `Supplies - ${description}`;
    default:
      return description;
  }
}

export function normalizeExpenseDescription(expense) {
  const description = normalizeSpaces(expense.description);
  const category = normalizeSpaces(expense.category);

  if (!description) return description;
  if (EXACT_RENAMES.has(description)) return EXACT_RENAMES.get(description);
  if (isAlreadyStructured(description)) return description;
  if (description === 'Misc') {
    return category === 'Other' ? 'Other - Misc' : category === 'Supplies' ? 'Supplies - Misc' : fallbackRename('Misc', category);
  }

  return fallbackRename(description, category);
}
